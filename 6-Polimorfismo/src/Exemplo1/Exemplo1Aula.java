package Exemplo1;

public class Exemplo1Aula {
	private String titulo;
	private int tempo;
	public Exemplo1Aula(String x, int y) {
		this.titulo=x;
		this.tempo=y;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public int getTempo() {
		return tempo;
	}
	public void setTempo(int tempo) {
		this.tempo = tempo;
	}
	
	
	
}
